public class StringExercise {
    public static void main(String[] args) {
        String lyrics = "We don't need no education \n We don't need non thought control";
        System.out.println(lyrics);

        String quote = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quote);

        String window = "In windows, the main drive is usually C:\\";
        System.out.println(window);

        String triple = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(triple);



    }
}
